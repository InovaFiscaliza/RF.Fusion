# =========================================================
# Adapter
# =========================================================

class AppAnaliseConnection:
    """
    Adapter for the external MATLAB-based `appAnalise` service.

    Responsibilities:
    1. Send protocol requests to the remote processor
    2. Validate the top-level response structure
    3. Normalize the returned spectra into RF.Fusion `bin_data`
    4. Resolve the output artifact generated by the service
    5. Apply the same global validations expected by the BIN pipeline

    This class is intentionally limited to protocol, normalization and
    output-artifact concerns. It does not perform database writes.
    """

    START_TAG = "<JSON>"
    END_TAG = "</JSON>"
    PROCESSOR_NAME = "appAnalise"
    MAX_RESPONSE_SIZE = 10 * 1024 * 1024
    NETWORK_RETRIES = 2
    INVALID_HOSTNAME_VALUES = {
        "none", "(none)", "null", "(null)", "unknown", ""
    }
    SOURCE_FILE_MISSING_SNIPPETS = (
        "filenotfound",
        "nosuchfile",
        "cannotfindfile",
        "pathnotfound",
    )
    RFEYE_CANONICAL_RE = re.compile(r"(rfeye\d{6})", re.IGNORECASE)
    GPS_SENTINEL_VALUE = -1

    def __init__(self) -> None:
        """
        Keep the latest protocol artifacts available for post-failure inspection.

        `process()` still raises on semantic or protocol errors, but callers can
        inspect these attributes afterwards to understand what appAnalise
        returned before the failure was classified.
        """
        self.bin_data: Dict[str, Any] = {}
        self.last_requested_file: Optional[str] = None
        self.last_response_text: Optional[str] = None
        self.last_payload: Optional[Dict[str, Any]] = None
        self.last_answer: Optional[Dict[str, Any]] = None
        self.last_file_info: Optional[Dict[str, Any]] = None
        self.last_output_meta: Optional[Dict[str, Any]] = None

    def _reset_last_result(self) -> None:
        """
        Clear per-request debug state before a new processing attempt.
        """
        self.bin_data = {}
        self.last_requested_file = None
        self.last_response_text = None
        self.last_payload = None
        self.last_answer = None
        self.last_file_info = None
        self.last_output_meta = None

    def _build_request_payload(self, full_path: str, export: bool) -> Dict:
        """
        Build the request payload expected by the appAnalise socket API.
        """
        return {
            "Key": k.APP_ANALISE_KEY,
            "ClientName": k.APP_ANALISE_CLIENT_NAME,
            "Request": {
                "type": "FileRead",
                "filepath": full_path,
                "export": export
            }
        }

    @staticmethod
    def _close_socket(sock: socket.socket) -> None:
        """
        Close a socket defensively without leaking cleanup exceptions.
        """
        try:
            sock.shutdown(socket.SHUT_RDWR)
        except Exception:
            pass

        try:
            sock.close()
        except Exception:
            pass

    # =================================================
    # Hostname helpers
    # =================================================

    def _is_valid_hostname(self, value):
        """
        Return whether a hostname extracted from the payload is usable.
        """

        if not isinstance(value, str):
            return False

        normalized = value.strip().lower()
        
        return normalized not in self.INVALID_HOSTNAME_VALUES

    def _normalize_hostname(self):
        """
        Normalize the receiver name into the hostname format expected by RF.Fusion.
        """

        raw_hostname = self.bin_data.get("hostname")
        hostname_valid = self._is_valid_hostname(raw_hostname)

        if not hostname_valid:
            raise errors.BinValidationError(
                "Hostname resolution failed: invalid hostname"
            )

        resolved = raw_hostname.strip()
        match = self.RFEYE_CANONICAL_RE.search(resolved)

        if match:
            resolved = match.group(1).lower()

        self.bin_data["hostname"] = resolved

    def _validate_hostname(self):
        """
        Ensure the normalized hostname is present and non-empty.
        """

        hostname = self.bin_data.get("hostname")

        if not isinstance(hostname, str) or not hostname.strip():
            raise errors.BinValidationError(
                "Hostname missing or invalid"
            )

    # =================================================
    # GPS validation
    # =================================================

    def _validate_gps(self):
        """
        Validate the normalized GPS object and coerce numeric fields to float.
        """

        gps = self.bin_data.get("gps")

        if gps is None:
            raise errors.BinValidationError("GPS metadata missing")

        for attr in ("latitude", "longitude", "altitude"):
            if not hasattr(gps, attr):
                raise errors.BinValidationError(
                    f"GPS missing attribute: {attr}"
                )

        try:
            lat = float(gps.latitude)
            lon = float(gps.longitude)
            alt = float(gps.altitude)
        except Exception:
            raise errors.BinValidationError(
                "GPS coordinates must be numeric"
            )

        if (
            lat == self.GPS_SENTINEL_VALUE
            and lon == self.GPS_SENTINEL_VALUE
            and (alt == self.GPS_SENTINEL_VALUE or alt == 0)
        ):
            raise errors.BinValidationError(
                "Invalid GPS reading: GNSS unavailable sentinel"
            )

        if not (-90.0 <= lat <= 90.0):
            raise errors.BinValidationError(
                "GPS invalid latitude range"
            )

        if not (-180.0 <= lon <= 180.0):
            raise errors.BinValidationError(
                "GPS invalid longitude range"
            )

        gps.latitude = lat
        gps.longitude = lon
        gps.altitude = alt

    # =================================================
    # Spectrum container validation
    # =================================================

    def _validate_spectrum_container(self):
        """
        Ensure the normalized spectrum list is iterable and non-empty.
        """

        spectra = self.bin_data.get("spectrum")

        if not isinstance(spectra, Iterable):
            raise errors.BinValidationError(
                "Spectrum is not iterable"
            )

        if not spectra:
            raise errors.BinValidationError(
                "Spectrum list is empty"
            )

    # =================================================
    # Socket communication
    # =================================================

    def check_connection(self) -> bool:
        """
        Perform a lightweight TCP reachability check against appAnalise.

        This is a preflight only. It confirms the endpoint is reachable,
        but it does not guarantee the service will remain healthy for the
        full processing request.
        """
        sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)

        try:
            sock.settimeout(k.APP_ANALISE_CONNECT_TIMEOUT)
            sock.connect((k.APP_ANALISE_HOST_ADD, k.APP_ANALISE_HOST_PORT))
            return True
        except Exception as e:
            raise errors.ExternalServiceTransientError(
                f"APP_ANALISE preflight connection failed: {e}"
            )
        finally:
            self._close_socket(sock)

    def _request_process(self, full_path: str, export: bool = False) -> Dict:
        """
        Submit a processing request and return the decoded protocol payload.
        """

        request_payload = self._build_request_payload(full_path, export)

        request_bytes = (
            json.dumps(request_payload, ensure_ascii=False) + "\r\n"
        ).encode("utf-8")

        last_error = None

        for _ in range(self.NETWORK_RETRIES):
            sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)

            try:
                sock.settimeout(k.APP_ANALISE_CONNECT_TIMEOUT)
                sock.connect(
                    (k.APP_ANALISE_HOST_ADD, k.APP_ANALISE_HOST_PORT)
                )
                sock.settimeout(k.APP_ANALISE_PROCESS_TIMEOUT)
                sock.sendall(request_bytes)
                raw_response = self._receive_all(sock)
                response_text = self._safe_decode(raw_response)
                self.last_response_text = response_text

                return self._extract_json(response_text)

            except Exception as e:
                last_error = e
                time.sleep(0.5)

            finally:
                self._close_socket(sock)

        raise errors.ExternalServiceTransientError(
            f"APP_ANALISE socket failure after retries: {last_error}"
        )

    def _receive_all(self, sock: socket.socket) -> bytes:
        """
        Read from the socket until the tagged JSON block is complete.
        """
        
        buffer = b""
        
        while True:
            try:
                chunk = sock.recv(k.APP_ANALISE_BUFFER_SIZE)
            except socket.timeout:
                raise errors.ExternalServiceTransientError(
                    "APP_ANALISE processing timeout"
                )

            if not chunk:
                break

            buffer += chunk

            if len(buffer) > self.MAX_RESPONSE_SIZE:
                raise errors.ExternalServiceTransientError(
                    "APP_ANALISE response exceeded maximum size"
                )

            start = buffer.find(self.START_TAG.encode())
            end = buffer.find(self.END_TAG.encode(), start)

            if start != -1 and end != -1:
                end += len(self.END_TAG)
                return buffer[start:end]

        raise errors.ExternalServiceTransientError(
            "APP_ANALISE response did not contain a complete JSON block"
        )

    # =================================================
    # JSON helpers
    # =================================================

    @staticmethod
    def _safe_decode(data: bytes) -> str:
        """
        Decode response bytes using UTF-8 with Latin-1 fallback.
        """

        try:
            return data.decode("utf-8")
        except UnicodeDecodeError:
            return data.decode("latin-1")

    def _extract_json(self, payload: str) -> Dict:
        """
        Extract the JSON payload wrapped by the protocol tags.
        """

        match = re.search(
            rf"{self.START_TAG}(.*?){self.END_TAG}",
            payload,
            re.DOTALL | re.IGNORECASE
        )

        if not match:
            raise errors.BinValidationError(
                "APP_ANALISE response missing JSON block"
            )

        try:
            return json.loads(match.group(1).strip())

        except json.JSONDecodeError as e:
            raise errors.BinValidationError(
                f"Invalid JSON returned by APP_ANALISE: {e}"
            )

    # =================================================
    # Protocol error detection
    # =================================================

    @classmethod
    def _is_missing_source_file_error(cls, message: object) -> bool:
        """
        Return whether an appAnalise error string points to a missing source file.

        The MATLAB side can report this in slightly different spellings, so we
        compare against a compact, normalized string instead of exact wording.
        """
        if not isinstance(message, str):
            return False

        normalized = "".join(message.strip().lower().split())
        return any(
            snippet in normalized for snippet in cls.SOURCE_FILE_MISSING_SNIPPETS
        )

    @staticmethod
    def _validate_source_file(full_path: str) -> None:
        """
        Ensure the requested source file still exists before contacting appAnalise.
        """
        if not os.path.isfile(full_path):
            raise errors.BinValidationError(
                f"APP_ANALISE source file unavailable before request: {full_path}"
            )

    def _raise_answer_error(
        self,
        answer_error: str,
        requested_full_path: Optional[str] = None,
    ) -> None:
        """
        Classify string errors returned in `Answer`.

        `FileNotFound` deserves an extra local filesystem check: if the source
        file still exists here, the failure looks like a service-side visibility
        problem and should be retried. If the file is truly gone locally, the
        task is no longer recoverable by reprocessing.
        """
        if self._is_missing_source_file_error(answer_error):
            if requested_full_path and os.path.isfile(requested_full_path):
                raise errors.ExternalServiceTransientError(
                    "APP_ANALISE reported missing source file, but it still "
                    f"exists locally: {requested_full_path} ({answer_error})"
                )

            if requested_full_path:
                raise errors.BinValidationError(
                    "APP_ANALISE reported missing source file and it is "
                    f"absent locally: {requested_full_path} ({answer_error})"
                )

        raise errors.BinValidationError(
            f"APP_ANALISE returned error in Answer: {answer_error}"
        )

    def _detect_protocol_error(
        self,
        payload: Dict,
        requested_full_path: Optional[str] = None,
    ):
        """
        Validate the top-level protocol structure before normalization begins.
        """

        if not isinstance(payload, dict):
            raise errors.BinValidationError(
                "APP_ANALISE returned invalid payload type"
            )

        # Protocol validation is structural only. Request strings may contain
        # words like "Error" in valid filenames, so we only inspect typed
        # protocol fields.
        err = payload.get("Error")

        if err is not None:

            if isinstance(err, (list, tuple)) and len(err) >= 2:
                code = err[0]
                message = err[1]
            else:
                code = "APP_ANALISE_ERROR"
                message = str(err)

            raise errors.BinValidationError(
                f"APP_ANALISE returned error: {code} - {message}"
            )

        answer = payload.get("Answer")

        if answer is None:
            raise errors.BinValidationError(
                "APP_ANALISE response missing Answer payload"
            )

        if isinstance(answer, str):
            self._raise_answer_error(answer, requested_full_path)

        if not isinstance(answer, dict):
            raise errors.BinValidationError(
                f"APP_ANALISE returned invalid Answer payload type: {answer}"
            )

        if not answer:
            raise errors.BinValidationError(
                f"APP_ANALISE returned empty Answer payload: {answer}"
            )

        if "Spectra" not in answer:
            raise errors.BinValidationError(
                f"APP_ANALISE response missing Answer.Spectra: {answer}"
            )

        spectra = answer.get("Spectra")

        if isinstance(spectra, str):
            raise errors.BinValidationError(
                f"APP_ANALISE returned invalid Spectra payload: {spectra}"
            )

        if not isinstance(spectra, (list, tuple)):
            raise errors.BinValidationError(
                f"APP_ANALISE returned invalid Answer.Spectra type: {spectra}"
            )

    def _get_answer(self, payload: Dict) -> Dict:
        """
        Return the validated `Answer` object from the protocol payload.
        """
        answer = payload.get("Answer")

        if not isinstance(answer, dict):
            raise errors.BinValidationError(
                f"APP_ANALISE returned invalid Answer payload type: {answer}"
            )

        return answer

    def _get_general_file_info(self, answer: Dict) -> Dict:
        """
        Extract the exported artifact location from `Answer.General`.
        """
        general = answer.get("General")

        if not isinstance(general, dict):
            raise errors.BinValidationError(
                f"APP_ANALISE response missing valid Answer.General: {general}"
            )

        path = general.get("FilePath")
        name = self._resolve_general_filename(general.get("FileName"))

        if not isinstance(path, str) or not path.strip():
            raise errors.BinValidationError(
                f"APP_ANALISE returned invalid General.FilePath: {path}"
            )

        if not isinstance(name, str) or not name.strip():
            raise errors.BinValidationError(
                f"APP_ANALISE returned invalid General.FileName: {name}"
            )

        return {
            "Path": path,
            "FileName": name,
        }

    @staticmethod
    def _resolve_general_filename(file_name) -> str:
        """
        Normalize `Answer.General.FileName` into a single filename string.

        appAnalise may return this field either as a single string or as a
        list such as `["name", ".mat"]`.
        """
        if isinstance(file_name, str):
            return file_name

        if isinstance(file_name, (list, tuple)) and file_name:
            return "".join(str(part) for part in file_name)

        return file_name

    def _build_output_file_info(
        self,
        answer: Dict,
        file_path: str,
        file_name: str,
        export: bool,
    ) -> Dict:
        """
        Resolve which filesystem artifact should be treated as the output file.

        When `export=True`, the output is the artifact described in
        `Answer.General`. Otherwise the original input file remains the
        relevant filesystem artifact.
        """
        if export:
            return self._get_general_file_info(answer)

        return {
            "Path": file_path,
            "FileName": file_name,
        }

    # =================================================
    # Normalize response → canonical bin_data
    # =================================================

    def _normalize_response(self, payload: Dict) -> Dict:
        """
        Convert a validated appAnalise payload into canonical RF.Fusion `bin_data`.
        """

        answer = self._get_answer(payload)
        spectra_payload = answer.get("Spectra", [])

        if not spectra_payload:
            raise errors.BinValidationError(
                "APP_ANALISE returned empty Spectra list"
            )

        spectrums = []
        hostname = None
        gps_obj = None
        method = None

        for spec in spectra_payload:
            metadata = spec.get("MetaData", {})
            gps = spec.get("GPS", {})
            related = spec.get("RelatedFiles", [])
            receiver = spec.get("Receiver")

            if hostname is None:
                hostname = receiver

            elif receiver != hostname:
                raise errors.BinValidationError(
                    "APP_ANALISE spectra contain inconsistent Receiver values"
                )

            if gps_obj is None:

                lat = float(gps.get("Latitude"))
                lon = float(gps.get("Longitude"))

                gps_obj = SimpleNamespace(
                    longitude=lon,
                    latitude=lat,
                    altitude=float(gps.get("Altitude", self.GPS_SENTINEL_VALUE)),
                    _longitude=[lon],
                    _latitude=[lat],
                    _altitude=[self.GPS_SENTINEL_VALUE]
                )

            start_mega = float(metadata["FreqStart"]) / 1e6
            stop_mega = float(metadata["FreqStop"]) / 1e6
            ndata = metadata["DataPoints"]

            for rf in related:
                if method is None:
                    task_name = rf.get("Task") or rf.get("task")
                    if isinstance(task_name, str) and task_name.strip():
                        # The effective processing recipe comes from the
                        # RelatedFiles task that generated the spectrum slice.
                        method = task_name.strip()

                try:
                    start_time = datetime.strptime(
                        rf.get("BeginTime"),
                        "%d-%b-%Y %H:%M:%S"
                    )

                    end_time = datetime.strptime(
                        rf.get("EndTime"),
                        "%d-%b-%Y %H:%M:%S"
                    )

                except Exception:
                    start_time = datetime.utcnow()
                    end_time = start_time

                trace_count = rf.get("NumSweeps")
                if trace_count is None:
                    trace_count = rf.get("nSweeps")

                spectrum = SimpleNamespace(
                    start_mega=start_mega,
                    stop_mega=stop_mega,
                    ndata=ndata,
                    trace_length=trace_count,
                    level_unit=metadata.get("LevelUnit"),
                    processing=metadata.get("TraceMode", "peak").lower(),
                    start_dateidx=start_time,
                    stop_dateidx=end_time,
                    bw=metadata.get("Resolution"),
                    description=rf.get("Description"),
                    metadata={}
                )

                spectrums.append(spectrum)

        return {
            "hostname": hostname,
            "method": method or answer.get("Method", self.PROCESSOR_NAME),
            "gps": gps_obj,
            "spectrum": spectrums
        }

    # =================================================
    # Output file metadata
    # =================================================

    def _resolve_output_file(self, file_info: Dict) -> Dict:
        """
        Wait for the output artifact to settle and return its filesystem metadata.

        Exported files can still be growing for a short period after the
        protocol response is emitted, so this method waits for the file size
        to stabilize before collecting stat metadata.
        """

        path = file_info.get("Path")
        name = file_info.get("FileName")
        full_path = os.path.join(path, name)
        try:
            last_size = os.path.getsize(full_path)
        except OSError as e:
            raise errors.ExternalServiceTransientError(
                f"APP_ANALISE output artifact unavailable: {full_path} ({e})"
            )

        for _ in range(10):
            time.sleep(0.2)
            try:
                size = os.path.getsize(full_path)
            except OSError as e:
                raise errors.ExternalServiceTransientError(
                    f"APP_ANALISE output artifact unavailable: {full_path} ({e})"
                )

            if size == last_size:
                break

            last_size = size

        try:
            stat = os.stat(full_path)
        except OSError as e:
            raise errors.ExternalServiceTransientError(
                f"APP_ANALISE output artifact unavailable: {full_path} ({e})"
            )

        return {
            "file_path": path,
            "file_name": name,
            "extension": os.path.splitext(name)[1].lower(),
            "size_kb": int(stat.st_size / 1024),
            "dt_created": datetime.fromtimestamp(stat.st_ctime),
            "dt_modified": datetime.fromtimestamp(stat.st_mtime),
            "full_path": full_path
        }

    # =================================================
    # Public API
    # =================================================

    def process(self, file_path: str, file_name: str, export: bool = False):
        """
        Process a source file through appAnalise.

        Returns:
            tuple[dict, dict]:
                - Canonical RF.Fusion `bin_data`
                - Filesystem metadata for the output artifact associated with
                  this processing request
        """
        full_path = os.path.join(file_path, file_name)
        self._reset_last_result()
        self.last_requested_file = full_path

        # If the source file already disappeared locally, recontacting the
        # external processor cannot make this task recoverable.
        self._validate_source_file(full_path)

        # The socket response defines both the normalized spectra and, when
        # export is enabled, the artifact that becomes relevant downstream.
        payload = self._request_process(full_path, export)
        self.last_payload = payload
        self._detect_protocol_error(
            payload,
            requested_full_path=full_path,
        )
        answer = self._get_answer(payload)
        self.last_answer = answer
        file_info = self._build_output_file_info(
            answer=answer,
            file_path=file_path,
            file_name=file_name,
            export=export,
        )
        self.last_file_info = file_info
        file_meta = self._resolve_output_file(file_info)
        self.last_output_meta = file_meta

        # Only after the protocol and output artifact are confirmed do we
        # normalize and validate the spectral payload.
        self.bin_data = self._normalize_response(payload)
        self._normalize_hostname()
        self._validate_hostname()
        self._validate_gps()
        self._validate_spectrum_container()

        return self.bin_data, file_meta

        v_row_count,
        CONCAT(
            'hosts=',
            (SELECT COUNT(*) FROM BPDATA.HOST),
            ';equipments=',
            (SELECT COUNT(*) FROM RFDATA.DIM_SPECTRUM_EQUIPMENT)
        ),
        NULL
    );

    DROP TEMPORARY TABLE IF EXISTS TMP_LINK_BEST;
    DROP TEMPORARY TABLE IF EXISTS TMP_LINK_CANDIDATES;
    DROP TEMPORARY TABLE IF EXISTS TMP_EQUIPMENT_KEYS;
    DROP TEMPORARY TABLE IF EXISTS TMP_HOST_KEYS;
END$$


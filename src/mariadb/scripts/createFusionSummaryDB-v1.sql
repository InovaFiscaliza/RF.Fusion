    SELECT
        h.FK_HOST,
        e.FK_EQUIPMENT,
        h.NA_HOST_NAME,
        e.NA_EQUIPMENT,
        h.NA_HOST_NAME_NORMALIZED,
        e.NA_EQUIPMENT_NAME_NORMALIZED,
        h.NA_HOST_SIGNATURE,
        e.NA_EQUIPMENT_SIGNATURE,
        'cwsm_signature',
        0.95,
        0
    FROM TMP_HOST_KEYS h
    JOIN TMP_EQUIPMENT_KEYS e
      ON h.NA_HOST_SIGNATURE IS NOT NULL
     AND h.NA_HOST_SIGNATURE <> ''
     AND h.NA_HOST_SIGNATURE = e.NA_EQUIPMENT_SIGNATURE;

    INSERT INTO TMP_LINK_CANDIDATES
    SELECT
        h.FK_HOST,
        e.FK_EQUIPMENT,
        h.NA_HOST_NAME,
        e.NA_EQUIPMENT,
        h.NA_HOST_NAME_NORMALIZED,
        e.NA_EQUIPMENT_NAME_NORMALIZED,

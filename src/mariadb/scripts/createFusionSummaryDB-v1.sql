        SELECT *
        FROM (
            SELECT
                c.*,
                ROW_NUMBER() OVER (
                    PARTITION BY c.FK_HOST, c.FK_EQUIPMENT
                    ORDER BY c.VL_MATCH_CONFIDENCE DESC, c.IS_MANUAL_OVERRIDE DESC, c.NA_MATCH_TYPE ASC

CREATE PROCEDURE PRC_REFRESH_HOST_EQUIPMENT_LINK_FULL()
BEGIN
    DECLARE v_started_at DATETIME DEFAULT UTC_TIMESTAMP();
    DECLARE v_row_count BIGINT DEFAULT 0;

    INSERT INTO SUMMARY_REFRESH_STATE (
        NA_OBJECT_NAME,
        DT_LAST_START,
        DT_LAST_END,
        IS_SUCCESS,
        NU_LAST_ROW_COUNT,
        NA_SOURCE_HIGH_WATERMARK,
        NA_ERROR_MESSAGE
    )
    VALUES (
        'HOST_EQUIPMENT_LINK',
        v_started_at,
        NULL,
        0,
        NULL,
        NULL,
        NULL
    )
    ON DUPLICATE KEY UPDATE
        DT_LAST_START = VALUES(DT_LAST_START),
        DT_LAST_END = VALUES(DT_LAST_END),
        IS_SUCCESS = VALUES(IS_SUCCESS),
        NU_LAST_ROW_COUNT = VALUES(NU_LAST_ROW_COUNT),
        NA_SOURCE_HIGH_WATERMARK = VALUES(NA_SOURCE_HIGH_WATERMARK),
        NA_ERROR_MESSAGE = VALUES(NA_ERROR_MESSAGE);

    DROP TEMPORARY TABLE IF EXISTS TMP_HOST_KEYS;
    DROP TEMPORARY TABLE IF EXISTS TMP_EQUIPMENT_KEYS;
    DROP TEMPORARY TABLE IF EXISTS TMP_LINK_CANDIDATES;
    DROP TEMPORARY TABLE IF EXISTS TMP_LINK_BEST;

    CREATE TEMPORARY TABLE TMP_HOST_KEYS AS
    SELECT
        h.ID_HOST AS FK_HOST,
        h.NA_HOST_NAME,
        FN_SUMMARY_NORMALIZE_KEY(h.NA_HOST_NAME) AS NA_HOST_NAME_NORMALIZED,

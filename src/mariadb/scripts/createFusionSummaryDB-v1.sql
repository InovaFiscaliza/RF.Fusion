        NA_EQUIPMENT_SIGNATURE VARCHAR(128) DEFAULT NULL,
        NA_MATCH_TYPE VARCHAR(32) NOT NULL,
        VL_MATCH_CONFIDENCE DECIMAL(5,2) NOT NULL,
        IS_MANUAL_OVERRIDE TINYINT(1) NOT NULL DEFAULT 0
    ) CHARACTER SET utf8mb4;

    INSERT INTO TMP_LINK_CANDIDATES

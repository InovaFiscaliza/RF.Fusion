CREATE FUNCTION FN_SUMMARY_NORMALIZE_KEY(p_value VARCHAR(255))
RETURNS VARCHAR(255)
DETERMINISTIC
BEGIN
    DECLARE v_value VARCHAR(255);

    SET v_value = LOWER(TRIM(COALESCE(p_value, '')));
    SET v_value = REPLACE(v_value, '-', '');
    SET v_value = REPLACE(v_value, '_', '');
    SET v_value = REPLACE(v_value, '.', '');
    SET v_value = REPLACE(v_value, ' ', '');
    SET v_value = REPLACE(v_value, '/', '');
    SET v_value = REPLACE(v_value, ':', '');
    SET v_value = REPLACE(v_value, ';', '');
    SET v_value = REPLACE(v_value, ',', '');
    SET v_value = REPLACE(v_value, '(', '');
    SET v_value = REPLACE(v_value, ')', '');
    SET v_value = REPLACE(v_value, '[', '');
    SET v_value = REPLACE(v_value, ']', '');

    RETURN v_value;
END$$

CREATE FUNCTION FN_SUMMARY_CWSM_SIGNATURE(p_normalized_key VARCHAR(255))
RETURNS VARCHAR(255)
DETERMINISTIC
BEGIN
    DECLARE v_digits VARCHAR(255);
    DECLARE v_prefix VARCHAR(4);

    IF p_normalized_key IS NULL OR p_normalized_key = '' THEN
        RETURN NULL;
    END IF;

    IF LEFT(p_normalized_key, 4) <> 'cwsm' THEN
        RETURN NULL;
    END IF;

    SET v_digits = SUBSTRING(p_normalized_key, 5);

    IF v_digits = '' OR v_digits REGEXP '[^0-9]' THEN
        RETURN NULL;
    END IF;

    IF CHAR_LENGTH(v_digits) < 6 THEN
        RETURN NULL;
    END IF;

    IF v_digits = '22010007' THEN
        RETURN 'cwsm211007';
    END IF;

    IF CHAR_LENGTH(v_digits) >= 8 THEN
        SET v_prefix = LEFT(v_digits, 4);

        IF v_prefix = '2110' THEN
            RETURN CONCAT('cwsm211', RIGHT(v_digits, 3));
        END IF;

        IF v_prefix = '2112' THEN
            RETURN CONCAT('cwsm212', RIGHT(v_digits, 3));
        END IF;

        IF v_prefix = '2201' THEN
            RETURN CONCAT('cwsm220', RIGHT(v_digits, 3));
        END IF;
    END IF;

    RETURN CONCAT('cwsm', LEFT(v_digits, 3), RIGHT(v_digits, 3));
END$$

CREATE FUNCTION FN_SUMMARY_MAP_STATE(
    p_is_host_known TINYINT,
    p_is_offline TINYINT,
    p_is_current_location TINYINT
)
RETURNS VARCHAR(32)
DETERMINISTIC
BEGIN
    IF COALESCE(p_is_host_known, 0) = 0 THEN
        RETURN 'no_host';
    END IF;

    IF COALESCE(p_is_offline, 0) = 1 THEN
        IF COALESCE(p_is_current_location, 0) = 1 THEN
            RETURN 'offline_current';
        END IF;

        RETURN 'offline_previous';
    END IF;

    IF COALESCE(p_is_current_location, 0) = 1 THEN
        RETURN 'online_current';
    END IF;

    RETURN 'online_previous';
END$$

CREATE FUNCTION FN_SUMMARY_MAP_STATE_PRIORITY(p_state VARCHAR(32))
RETURNS SMALLINT
DETERMINISTIC
BEGIN
    IF p_state = 'online_current' THEN
        RETURN 0;
    END IF;

    IF p_state = 'online_previous' THEN
        RETURN 1;
    END IF;

    IF p_state = 'offline_current' THEN
        RETURN 2;
    END IF;

    IF p_state = 'offline_previous' THEN
        RETURN 3;
    END IF;

    RETURN 4;
END$$

